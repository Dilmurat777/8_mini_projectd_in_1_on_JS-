let filterList = [];

searchInput.addEventListener('input', () => {
  filterSearch(searchInput.value, countries);
  renderCountry(filterList);
});

function filterSearch(input, list) {
  filterList = list.filter((country) => {
    return country.toLowerCase().includes(input.toLowerCase())
  });
}

function renderCountry(list) {
	searchCountries.innerHTML = ''
  filterList = list.forEach((country) => {
    const elementDiv = document.createElement('div');
    elementDiv.classList.add('templateDiv');
    searchCountries.appendChild(elementDiv);
    elementDiv.textContent = country;
  });
}

